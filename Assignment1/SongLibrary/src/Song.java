/*
 * This class stores song-related data
 * @author MaxHumes
 */

public class Song 
{
	private String name;
	private String artist;
	private String album;
	private int year;
	
	public Song(String name, String artist, String album, int year)
	{
		this.name = name;
		this.artist = artist;
		this.album = album;
		this.year = year;
	}
}
